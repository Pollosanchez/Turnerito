const uuid = require('uuid/v1')

class Paciente {
    constructor(nombre, apellido, dni, tel, mail, direccion, osID, f_nacimiento) {
        this._id = uuid()
        this.nombre = nombre
        this.apellido = apellido
        this.dni = dni
        this.tel = tel
        this.mail = mail
        this.direccion = direccion
        this.osID = osID
        this.f_nacimiento = f_nacimiento
    }
}

module.exports = Paciente